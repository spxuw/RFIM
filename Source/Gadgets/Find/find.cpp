// find the position of the element j in the list nbl  (assume j is definitely in the list)
int findpos(Nbl& nbl, int j)
{
  int pos=0;
  for(Nbl::iterator p = nbl.begin(); p!= nbl.end(); p++) {
    if(j==(*p)) 
      return pos;
    else
      pos++;
  }

  return -1;
}
